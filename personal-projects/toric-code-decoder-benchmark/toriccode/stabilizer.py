"""Periodic (L x L) toric code lattice, restricted to the CSS X-error / Z-stabilizer
(plaquette) sector.

Qubits sit on edges of an L x L grid with periodic boundary conditions:

  * ``H[r, c]`` is the qubit on the horizontal edge from vertex ``(r, c)`` to
    ``(r, c+1 mod L)``.
  * ``V[r, c]`` is the qubit on the vertical edge from vertex ``(r, c)`` to
    ``(r+1 mod L, c)``.

There are ``2 * L * L`` qubits and ``L * L`` plaquette (Z-type) stabilizers, each
one, ``B_{r,c}``, built from the four edges bounding plaquette ``(r, c)``:
``H[r, c]``, ``H[r+1, c]``, ``V[r, c]``, ``V[r, c+1]`` (indices mod L).

For CSS codes the two error sectors (X errors / Z-stabilizer syndromes, and Z
errors / X-stabilizer syndromes) decouple completely, so restricting to one
sector is the standard simplification used when benchmarking decoders (see e.g.
Dennis, Kitaev, Landahl & Preskill, "Topological quantum memory", J. Math. Phys.
43, 4452 (2002); Delfosse & Nickerson, Quantum 5, 595 (2021)).
"""

from __future__ import annotations

from dataclasses import dataclass

import numpy as np


@dataclass
class ToricLattice:
    """A distance-``L`` periodic toric code, X-error sector only."""

    L: int

    def __post_init__(self) -> None:
        if self.L < 3:
            raise ValueError("L must be >= 3")
        self._build_vertex_operator_rref()

    # ------------------------------------------------------------------
    # Syndrome extraction
    # ------------------------------------------------------------------
    def syndrome(self, h_err: np.ndarray, v_err: np.ndarray) -> np.ndarray:
        """Return the L x L plaquette syndrome for an X-error pattern.

        ``h_err`` / ``v_err`` are boolean (or 0/1) L x L arrays marking which
        qubits have suffered an X error.
        """
        h_err = h_err.astype(np.uint8)
        v_err = v_err.astype(np.uint8)
        return (
            h_err
            ^ np.roll(h_err, -1, axis=0)
            ^ v_err
            ^ np.roll(v_err, -1, axis=1)
        )

    def defect_list(self, syndrome: np.ndarray) -> list[tuple[int, int]]:
        rs, cs = np.nonzero(syndrome)
        return list(zip(rs.tolist(), cs.tolist()))

    # ------------------------------------------------------------------
    # Dual-lattice paths (used by decoders to build correction chains)
    # ------------------------------------------------------------------
    def dual_step_edges(self, r: int, c: int, dr: int, dc: int):
        """Edges crossed moving one dual step (dr, dc) in {-1,0,1}^2 (one axis
        nonzero) from plaquette (r, c). Returns (kind, rr, cc) where kind is
        'h' or 'v'.
        """
        L = self.L
        if dc == 1:
            return ("v", r % L, (c + 1) % L)
        if dc == -1:
            return ("v", r % L, c % L)
        if dr == 1:
            return ("h", (r + 1) % L, c % L)
        if dr == -1:
            return ("h", r % L, c % L)
        raise ValueError("dual step must move exactly one unit along one axis")

    def dual_path_toggles(self, p1: tuple[int, int], p2: tuple[int, int]):
        """Edges toggled by a shortest (torus-wrapping) dual-lattice path
        between two plaquettes, moving first along the row, then the column.

        Returns a list of (kind, r, c) tuples, kind in {'h', 'v'}.
        """
        L = self.L
        r1, c1 = p1
        r2, c2 = p2
        toggles = []

        dc_raw = (c2 - c1) % L
        if dc_raw != 0:
            step = 1 if dc_raw <= L - dc_raw else -1
            n_steps = dc_raw if step == 1 else L - dc_raw
            c = c1
            for _ in range(n_steps):
                toggles.append(self.dual_step_edges(r1, c, 0, step))
                c = (c + step) % L

        dr_raw = (r2 - r1) % L
        if dr_raw != 0:
            step = 1 if dr_raw <= L - dr_raw else -1
            n_steps = dr_raw if step == 1 else L - dr_raw
            r = r1
            for _ in range(n_steps):
                toggles.append(self.dual_step_edges(r, c2, step, 0))
                r = (r + step) % L

        return toggles

    @staticmethod
    def torus_distance(p1: tuple[int, int], p2: tuple[int, int], L: int) -> int:
        dr = abs(p1[0] - p2[0])
        dr = min(dr, L - dr)
        dc = abs(p1[1] - p2[1])
        dc = min(dc, L - dc)
        return dr + dc

    def apply_toggles(self, h_arr: np.ndarray, v_arr: np.ndarray, toggles) -> None:
        for kind, r, c in toggles:
            if kind == "h":
                h_arr[r, c] ^= 1
            else:
                v_arr[r, c] ^= 1

    # ------------------------------------------------------------------
    # Stabilizer-equivalence test (is a chain a trivial X-stabilizer element,
    # i.e. generated by vertex "star" operators, or a nontrivial logical op?)
    # ------------------------------------------------------------------
    def _build_vertex_operator_rref(self) -> None:
        """Precompute a row-reduced echelon basis (mod 2) for the row space of
        the vertex-operator ("star") check matrix H_X.

        Vertex (r, c)'s star operator acts on its four incident edges:
        H[r, c], H[r, c-1], V[r, c], V[r-1, c] (indices mod L).

        A chain is a trivial (stabilizer-equivalent-to-identity) X-error
        pattern iff it lies in the row space of H_X; otherwise it represents a
        nontrivial logical X operator. This is exact linear algebra over
        GF(2), used instead of an ad-hoc geometric "winding number" formula to
        avoid subtle off-by-one/parity bugs.
        """
        L = self.L
        n = 2 * L * L

        def h_idx(r, c):
            return (r % L) * L + (c % L)

        def v_idx(r, c):
            return L * L + (r % L) * L + (c % L)

        rows = []
        for r in range(L):
            for c in range(L):
                row = np.zeros(n, dtype=np.uint8)
                row[h_idx(r, c)] ^= 1
                row[h_idx(r, c - 1)] ^= 1
                row[v_idx(r, c)] ^= 1
                row[v_idx(r - 1, c)] ^= 1
                rows.append(row)
        matrix = np.array(rows, dtype=np.uint8)

        pivots = []  # list of (pivot_col, row_vector)
        work = matrix.copy()
        n_rows = work.shape[0]
        row_ptr = 0
        for col in range(n):
            pivot_row = None
            for r in range(row_ptr, n_rows):
                if work[r, col]:
                    pivot_row = r
                    break
            if pivot_row is None:
                continue
            work[[row_ptr, pivot_row]] = work[[pivot_row, row_ptr]]
            for r in range(n_rows):
                if r != row_ptr and work[r, col]:
                    work[r] ^= work[row_ptr]
            pivots.append((col, work[row_ptr].copy()))
            row_ptr += 1
            if row_ptr == n_rows:
                break

        self._n_qubits = n
        self._pivots = pivots  # sorted by pivot column ascending

    def chain_to_vector(self, h_arr: np.ndarray, v_arr: np.ndarray) -> np.ndarray:
        L = self.L
        return np.concatenate(
            [h_arr.astype(np.uint8).ravel(), v_arr.astype(np.uint8).ravel()]
        )

    def is_trivial(self, h_arr: np.ndarray, v_arr: np.ndarray) -> bool:
        """True iff (h_arr, v_arr), as an X-error chain, is generated by
        vertex star operators (i.e. is undetectable AND not a logical
        operator -- a "harmless" stabilizer element)."""
        vec = self.chain_to_vector(h_arr, v_arr).copy()
        for pivot_col, row in self._pivots:
            if vec[pivot_col]:
                vec ^= row
        return not vec.any()

    @property
    def stabilizer_rank(self) -> int:
        """rank(H_X); should be L*L - 1 (one redundancy: product of all star
        operators is the identity)."""
        return len(self._pivots)
